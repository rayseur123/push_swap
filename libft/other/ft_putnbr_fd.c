#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	long int	x;

	x = (long int)n;
	if (x < 0)
	{
		write(fd, "-", 1);
		x *= -1;
	}
	if (x <= 9)
	{
		x = x + '0';
		write(fd, &x, 1);
	}
	else
	{
		ft_putnbr_fd((int)(x / 10), fd);
		write (fd, &"0123456789"[x % 10], 1);
	}
}
