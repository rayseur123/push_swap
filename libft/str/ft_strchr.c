char	*ft_strchr(const char *s, int c)
{
	int	x;

	x = 0;
	while (s[x] != '\0')
	{
		if (s[x] == (char)c)
			return (((char *)&s[x]));
		x++;
	}
	if ((char)c == '\0')
		return (((char *)&s[x]));
	return (0);
}
