#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	x;
	int	len;

	x = 0;
	len = ft_strlen(s);
	while (x < len)
	{
		f(x, &s[x]);
		x++;
	}
}
