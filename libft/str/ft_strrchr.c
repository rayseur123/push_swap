char	*ft_strrchr(const char *s, int c)
{
	int		x;
	char	*val;

	val = 0;
	x = 0;
	while (s[x] != '\0')
	{
		if (s[x] == (char)c)
			val = (char *)&s[x];
		x++;
	}
	if ((char)c == '\0')
		return (((char *)&s[x]));
	else
		return (val);
}
